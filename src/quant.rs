pub fn dequant_iq3_xxs(bytes: &[u8], shape: Vec<usize>) -> Tensor {
    const BLOCK_SIZE: usize = 98; // 2 (d) + 64 (qs) + 32 (scales_and_signs)
    assert_eq!(
        bytes.len() % BLOCK_SIZE,
        0,
        "IQ3_XXS data size must be a multiple of 98 bytes"
    );

    let numel: usize = shape.iter().product();
    let mut data = vec![0f32; numel];
    let mut out_idx = 0;

    for block in bytes.chunks_exact(BLOCK_SIZE) {
        let d = f16::from_le_bytes([block[0], block[1]]).to_f32();
        let qs = &block[2..2 + 64];
        let scales_and_signs = &block[2 + 64..2 + 96];

        for ib32 in 0..8 {
            let sas = &scales_and_signs[4 * ib32..4 * ib32 + 4];
            let aux32 = u32::from_le_bytes([sas[0], sas[1], sas[2], sas[3]]);
            let db = d * (0.5 + ((aux32 >> 28) as f32)) * 0.5;

            let qs_ib32 = &qs[8 * ib32..8 * ib32 + 8];

            for l in 0..4 {
                let signs = KSIGNS_IQ2XS[((aux32 >> (7 * l)) & 127) as usize];
                let g1 = IQ3XXS_GRID[qs_ib32[2 * l] as usize].to_le_bytes();
                let g2 = IQ3XXS_GRID[qs_ib32[2 * l + 1] as usize].to_le_bytes();

                let base = out_idx + 8 * l;
                for j in 0..4 {
                    let s0 = if signs & KMASK_IQ2XS[j] != 0 {
                        -1.0
                    } else {
                        1.0
                    };
                    let s1 = if signs & KMASK_IQ2XS[j + 4] != 0 {
                        -1.0
                    } else {
                        1.0
                    };
                    data[base + j] = db * (g1[j] as f32) * s0;
                    data[base + j + 4] = db * (g2[j] as f32) * s1;
                }
            }
            out_idx += 32;
        }
    }

    Tensor::from_vec(data, shape)
}

