pub fn dequant_iq2_s(bytes: &[u8], shape: Vec<usize>) -> Tensor {
    const BLOCK_SIZE: usize = 82;
    assert_eq!(
        bytes.len() % BLOCK_SIZE,
        0,
        "IQ2_S data size must be a multiple of 82 bytes"
    );

    let numel: usize = shape.iter().product();
    let mut data = vec![0f32; numel];
    let mut out_idx = 0;

    for block in bytes.chunks_exact(BLOCK_SIZE) {
        let d = f16::from_le_bytes([block[0], block[1]]).to_f32();
        let qs_full = &block[2..66];
        let qh = &block[66..74];
        let scales = &block[74..82];

        for ib32 in 0..8 {
            let db0 = d * (0.5 + ((scales[ib32] & 0x0F) as f32)) * 0.25;
            let db1 = d * (0.5 + ((scales[ib32] >> 4) as f32)) * 0.25;

            let qs = &qs_full[4 * ib32..4 * ib32 + 4];
            let signs = &qs_full[32 + 4 * ib32..32 + 4 * ib32 + 4];

            for l in 0..4 {
                let dl = if l < 2 { db0 } else { db1 };
                let grid_idx = (qs[l] as u32) | (((qh[ib32] as u32) << (8 - 2 * l)) & 0x300);
                let grid = IQ2S_GRID[grid_idx as usize].to_le_bytes();
                let base = out_idx + 8 * l;
                for j in 0..8 {
                    let s = if signs[l] & KMASK_IQ2XS[j] != 0 {
                        -1.0
                    } else {
                        1.0
                    };
                    data[base + j] = dl * (grid[j] as f32) * s;
                }
            }
            out_idx += 32;
        }
    }

    Tensor::from_vec(data, shape)
}
