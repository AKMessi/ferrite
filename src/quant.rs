pub fn dequant_iq3_s(bytes: &[u8], shape: Vec<usize>) -> Tensor {
    const BLOCK_SIZE: usize = 110;
    assert_eq!(
        bytes.len() % BLOCK_SIZE,
        0,
        "IQ3_S data size must be a multiple of 110 bytes"
    );

    let numel: usize = shape.iter().product();
    let mut data = vec![0f32; numel];
    let mut out_idx = 0;

    for block in bytes.chunks_exact(BLOCK_SIZE) {
        let d = f16::from_le_bytes([block[0], block[1]]).to_f32();
        let qs_all = &block[2..66];
        let qh_all = &block[66..74];
        let signs_all = &block[74..106];
        let scales = &block[106..110];

        let mut qs_off = 0usize;
        let mut signs_off = 0usize;

        for p in 0..4 {
            let qh0 = qh_all[2 * p] as u32;
            let qh1 = qh_all[2 * p + 1] as u32;

            let db1 = d * (1.0 + 2.0 * ((scales[p] & 0x0F) as f32));
            let db2 = d * (1.0 + 2.0 * ((scales[p] >> 4) as f32));

            for (qh, db) in [(qh0, db1), (qh1, db2)] {
                let qs = &qs_all[qs_off..qs_off + 8];
                let signs = &signs_all[signs_off..signs_off + 4];

                for l in 0..4 {
                    let idx1 = (qs[2 * l] as u32) | ((qh << (8 - 2 * l)) & 256);
                    let idx2 = (qs[2 * l + 1] as u32) | ((qh << (7 - 2 * l)) & 256);
                    let grid1 = IQ3S_GRID[idx1 as usize].to_le_bytes();
                    let grid2 = IQ3S_GRID[idx2 as usize].to_le_bytes();
                    let base = out_idx + 8 * l;
                    for j in 0..4 {
                        let s0 = if signs[l] & KMASK_IQ2XS[j] != 0 {
                            -1.0
                        } else {
                            1.0
                        };
                        let s1 = if signs[l] & KMASK_IQ2XS[j + 4] != 0 {
                            -1.0
                        } else {
                            1.0
                        };
                        data[base + j] = db * (grid1[j] as f32) * s0;
                        data[base + j + 4] = db * (grid2[j] as f32) * s1;
                    }
                }
                out_idx += 32;
                qs_off += 8;
                signs_off += 4;
            }
        }
    }

    Tensor::from_vec(data, shape)
}

pub fn dequant_q2_k(bytes: &[u8], shape: Vec<usize>) -> Tensor {
    const BLOCK_SIZE: usize = 84;
    assert_eq!(
        bytes.len() % BLOCK_SIZE,
        0,
        "Q2_K data size must be a multiple of 84 bytes"
    );

    let numel: usize = shape.iter().product();
    let mut data = Vec::with_capacity(numel);

    for block in bytes.chunks_exact(BLOCK_SIZE) {
        let scales = &block[0..16];
        let q = &block[16..80];
        let d = f16::from_le_bytes([block[80], block[81]]).to_f32();
        let dmin = f16::from_le_bytes([block[82], block[83]]).to_f32();

        let mut is = 0usize;
        for n_block in 0..2 {
            let q_win = &q[n_block * 32..n_block * 32 + 32];
            let mut shift = 0u32;

            for _j in 0..4 {
                let sc = scales[is];
                is += 1;
                let dl = d * ((sc & 0x0F) as f32);
                let ml = dmin * ((sc >> 4) as f32);
                for l in 0..16 {
                    let qv = ((q_win[l] >> shift) & 3) as f32;
                    data.push(dl * qv - ml);
                }

                let sc = scales[is];
                is += 1;
                let dl = d * ((sc & 0x0F) as f32);
                let ml = dmin * ((sc >> 4) as f32);
                for l in 0..16 {
                    let qv = ((q_win[l + 16] >> shift) & 3) as f32;
                    data.push(dl * qv - ml);
                }

                shift += 2;
            }
        }
    }

    Tensor::from_vec(data, shape)
}
